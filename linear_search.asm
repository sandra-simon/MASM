ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	STRING1 DB 11H,22H,33H,44H,55H
	MSG1 DB "FOUND$"
	MSG2 DB "NOT FOUND$"
	SE DB 22H
DATA ENDS

PRTMSG MACRO MESSAGE
	MOV AH,09H
	LEA DX,MESSAGE
	INT 21H
	INT 3
ENDM

CODE SEGMENT
	START:
	MOV AX,DATA
	MOV DS,AX

	MOV AL,SE
	LEA SI,STRING1
	MOV CX,04H

	UP:
	MOV BL,[SI]
	CMP AL,BL
	JZ FO
	INC SI
	DEC CX
	JNZ UP

	PRTMSG MSG2
	JMP END1
	FO:
	PRTMSG MSG1

	END1: MOV AH,4CH
	INT 21H
CODE ENDS

END START